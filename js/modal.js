function javacourse() {

  modal = document.getElementById('javacourse')

  modal.classList.add('abrir')

  modal.addEventListener('Clicker', (e) => {

    if (e.target.id == 'fechar') {

      modal.classList.remove('abrir')
    }
  })
}

